/**
 * Immutable data class representing a color family.
 *
 * A color family groups related colors that work together in the Material Design 3 system,
 * including a base color, its on-color contrast, and container variants.
 *
 * @property color The base color of the family
 * @property onColor The color to use for content displayed on top of the base color
 * @property colorContainer A tonal variant of the base color for containers
 * @property onColorContainer The color to use for content displayed on colorContainer
 */
@Immutable
data class ColorFamily(
    val color: Color,
    val onColor: Color,
    val colorContainer: Color,
    val onColorContainer: Color
)

/**
 * Unspecified color family for initialization.
 *
 * Used as a placeholder or default value when a color family has not been specified.
 */
val unspecified_scheme = ColorFamily(
    Color.Unspecified, Color.Unspecified, Color.Unspecified, Color.Unspecified
)

/**
 * GeoAssist application theme composable.
 *
 * This theme wraps all application content and provides Material Design 3 theming including
 * color schemes, typography, and component styles. It supports both light and dark themes
 * based on system preferences.
 *
 * IMPORTANT: Dynamic color is DISABLED by default to ensure the custom GeoAssist color
 * palette is always used, maintaining consistent branding across all Android versions.
 *
 * @param darkTheme Whether to use the dark theme variant. Defaults to system dark theme setting.
 * @param dynamicColor Whether to use dynamic colors from user wallpaper. DEFAULT: false (disabled)
 *                     to enforce custom color scheme. Only supported on Android 12+.
 * @param content The composable content to be themed
 */
@Composable
fun AppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        
        darkTheme -> darkScheme
        else -> lightScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = AppTypography,
        content = content
    )
}